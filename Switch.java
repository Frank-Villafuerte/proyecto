public class Switch{
	public static void main(String[] args){
		
		int numero=5;

		switch(numero){
			case 1:
				System.out.print("Uno");
				break;
			case 2:	
				System.out.print("Dos");
				break;
		
			case 3:
				System.out.print("Tres");
				break;
			case 4:
				System.out.print("Cuatro");
				break;
			case 5:
				System.out.print("Cinco");
				break;
			case 6:
				System.out.print("Seis");
				break;
			case 7:
				System.out.print("Siete");
				break;
		}
	}
}

